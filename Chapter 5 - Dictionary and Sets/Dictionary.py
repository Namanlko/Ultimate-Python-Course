# Dictionary is a collection of key-value pairs.

dict = {
    "Key": "Value",
    "Angela": "Code",
    "Marks": 100,
    "List": [1,2,3]
}

print(dict["Key"]) # Prints "Value"
print(dict["List"]) # Prints [1,2,3]

# Properties of a Python Dictionaries:
# 1) It is unordered.
# 2) It is mutable.
# 3) It is indexed.
# 4) Cannot contain duplicate key.