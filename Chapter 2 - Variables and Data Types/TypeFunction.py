# Type Function: type() function is used to find the data type of a given variable in Python.

a = 31
print(type(a))

b = "31"
print(type(b))

# Note - A number can be converted into a String and vice-versa (if possible).

# There are many functions to convert one data type into another.

print(type(str(31)))
print(type(int("31")))
print(type(float(32)))

# Here "31" is a string literal and 31 is a numeric literal.