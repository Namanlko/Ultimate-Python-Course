# Rules for Defining a Variable name are:

# Rules for defining a variable name also applies to other identifers.
# 1) A varibale name can contain alphabets, digits and underscores.
# 2) A variable name cannot start with a digit.
# 3) A variable name can only start with an alphabet and underscores
# 4) No white spaces are allowed to be used inside a variable name.
# 5) Always try to give a meaningful name to a variable.

