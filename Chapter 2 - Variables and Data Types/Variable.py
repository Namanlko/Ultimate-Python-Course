# Variable: Variable is the name given to a memory location in a program. Let us consider this with the help of example as shown below.

a = "Angela Yu" # Here variable a holds the string value "Angela Yu".
print(a)

b = 5 # Here variable b holds the integer value 5.
print(b)

c = True # Here variable c holds the boolean value True.
print(c)

# So in very simple words we can say that variables are the containers to store a value.