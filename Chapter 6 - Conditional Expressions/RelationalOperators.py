# Relational Operators: Relational Operators are used to evaluate condtions inside the if statements. Some examples of relational operators are:

a = 10
b = 5
c = 10

# 1) Equal To
print(a==c)

# 2) Greater Than or Equal To
print(a>=b)

# 3) Less Than or Equal To
print(a<=b)

# 4) Greater Than
print(a>b)

# 5) Less Than
print(a<b)

# 6) Not Equal To
print(a==c)