# String Slicing: A String in python can be sliced for getting a part of the string.
# Let's try to understand String Slicing with the help of an example.

str = "Angela Yu"

print(str[1:5])
print(str[2:5])
print(str[1:5:2]) # With Skip Value by 2.

# Note: Python also support negative indexing.

# Other Advance Slicing Technique:

st = "amazing"
print(st[:7])
print(st[0:])