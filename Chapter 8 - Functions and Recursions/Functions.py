# Function: A function is a block of code which is used to perfom a particular task when it is called. A function can be used by the programmer in a given program any number of times.

# Function Defination: The part of program containing the exact set of instructions which are executed during the function call.

# Function Call: Whenever we want to call a function, we put the name of the function followed by the parenthesis as follows.

# Example

# Function Definition:
def fun():
    print("Hello Python!")

# Function Call:
fun()