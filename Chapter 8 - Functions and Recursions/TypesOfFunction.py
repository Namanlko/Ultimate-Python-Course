# Types of Function: There are two types of functions in python.
# 1) Built In Functions
# 2) User Defined Functions

# Built In Function: Built In Function are those functions which are present in python and user can used it according to their requirement.

# Example:
print("Hello Functions!")

str = "Angela Yu"
print(len(str))

# User Defined Function: User Defined function are those function which is defined by the user according to their requirement.

# Example:
def Sum(a,b):
    return a+b;

print(Sum(8,5))