# Tuple: Tuple is an immutable (cannot change) data type in python. Once defined tuple element can't be altered or manipulated.

a = ()           # Empty Tuple
b = (1,)         # Tuple with the only one element needs a comma.
c = (1,2,3,4,5)  # Tuple with more than one element.

print(a)
print(b)
print(c)
