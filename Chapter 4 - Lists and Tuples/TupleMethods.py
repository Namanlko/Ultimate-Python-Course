# Tuple Methods:
# Consider the following tuple.

tup = (1,7,2,4,1,0,45,67,2,34)

# 1) Count Function: It is used to return the occurence of a particular value in a tuple.
print(tup.count(1))

# 2) Index Function: It is will return the index of first occurnce of given value of the tuple.
print(tup.index(1))